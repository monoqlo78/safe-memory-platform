"""Human-readable ``message`` field on JobResponse.

The ``message`` line is generated by ``job_to_response`` so models relay it
verbatim and always surface the download link when a pack is ready, independent
of any GPT/Claude instructions.
"""

from __future__ import annotations

from app.main import app
from app.models.job_schema import (
    JobRecord,
    JobStatus,
    RetentionMode,
    job_to_response,
)


def test_message_completed_contains_download_link():
    job = JobRecord(
        job_id="j1",
        agent_id="a1",
        pack_id="my-pack",
        status=JobStatus.COMPLETED,
        retention_mode=RetentionMode.SERVER_VAULT,
        entry_count=28,
        download_token="tok_abc123",
    )
    resp = job_to_response(job)
    assert resp.message is not None
    assert "Download" in resp.message
    assert "/api/packs/dl/" in resp.message
    assert resp.download_url is not None
    assert resp.download_url in resp.message


def test_message_processing_mentions_getjob_and_no_download():
    job = JobRecord(
        job_id="j2",
        agent_id="a1",
        pack_id="my-pack",
        status=JobStatus.PROCESSING,
        retention_mode=RetentionMode.PROCESS_AND_RETURN,
    )
    resp = job_to_response(job)
    assert resp.message is not None
    assert "getJob" in resp.message
    assert "j2" in resp.message
    assert resp.download_url is None


def test_message_failed_contains_failed():
    job = JobRecord(
        job_id="j3",
        agent_id="a1",
        pack_id="my-pack",
        status=JobStatus.FAILED,
        retention_mode=RetentionMode.SESSION,
        warnings=["No supported files found."],
    )
    resp = job_to_response(job)
    assert resp.message is not None
    assert "failed" in resp.message.lower()
    assert "No supported files found." in resp.message


def test_openapi_jobresponse_declares_message():
    schema = app.openapi()
    props = schema["components"]["schemas"]["JobResponse"]["properties"]
    assert "message" in props
    build = schema["paths"]["/api/packs/build-from-url"]["post"]
    body = build["responses"]["200"]["content"]["application/json"]["schema"]
    assert body.get("$ref", "").endswith("/JobResponse")
